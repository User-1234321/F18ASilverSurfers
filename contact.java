public class contact {
    private String name;
    private int telephone;
    private String email;

    //constructor
    public contact(String name, int telephone, String email){
        this.name = name;
        this.telephone = telephone;
        this.email = email;
    }

    //getters
    public String getName(){
        return name;
    }

    public int getTelephone(){
        return telephone;
    }

    public String getEmail(){
        return email;
    }

    //setters


}
